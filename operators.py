# Arithmetic operators- simple calculation
num1 = 56
num2 = 8
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)# modulus returns the reminder

# comparison operators -compare values
print(num1<num2)
print(num1>num2)
print(num1<=num2)
print(num1>=num2)
print(num1==num2)
print(num1!=num2)

# Assignment Operators -Assign values to variables
a = 200
print(a)

a += 20
print(a)
a -= 20
print(a)

# logic operators -and ,or,not
x = 100
print( x < 250 and x > 1000)
print( x < 250 or x > 1000)
print( not (x < 250 or x > 1000))

# Operator precedence -order in which operations get executed
output = ( 100-4*3/1)

print(int(output))

